package ma.azdad.model;

import java.util.Arrays;

class Niahaha {

	private String name;

	private String[] other;

	public Niahaha(String name, String[] other) {
		super();
		this.name = name;
		this.other = other;
	}

	@Override
	public String toString() {
		return "Niahaha [name=" + name + ", other=" + Arrays.toString(other) + "]";
	}

}